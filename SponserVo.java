package Vo;

public class SponserVo {

	private int sponserId;
	private String sponserName;
	private attachmentVo attachmentVo;
	
	public int getSponserId() {
		return sponserId;
	}
	public void setSponserId(int sponserId) {
		this.sponserId = sponserId;
	}
	public String getSponserName() {
		return sponserName;
	}
	public void setSponserName(String sponserName) {
		this.sponserName = sponserName;
	}
	public attachmentVo getAttachmentVo() {
		return attachmentVo;
	}
	public void setAttachmentVo(attachmentVo attachmentVo) {
		this.attachmentVo = attachmentVo;
	}
	
	
}
